class Solution
{
public:
    vector<int> singleNumber(vector<int> nums) 
    {
        unordered_set<int> st;
        for (auto i : nums) {
            if (st.find(i) != st.end()) {
                st.erase(i);
            } else {
                st.insert(i);
            }
        }
        
        vector<int> result(st.begin(), st.end());
        sort(result.begin(), result.end());
        return result;
    }
};

