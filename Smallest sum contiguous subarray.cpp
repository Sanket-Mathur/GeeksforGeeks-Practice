class Solution{
  public:
  int smallestSumSubarray(vector<int>& a){
      int minSum = INT_MAX, currSum = 0;
      for (auto i : a) {
          currSum += i;
          minSum = min(minSum, currSum);
          
          if (currSum > 0) {
              currSum = 0;
          }
      }
      
      return minSum;
  }
};


