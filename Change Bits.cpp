class Solution {
  public:
    vector<int> changeBits(int N) {
        int bitnum = 0, temp = N;
        while (temp > 0) {
            temp >>= 1;
            ++bitnum;
        }
        
        int modified = (1 << bitnum) - 1;
        
        return {modified - N, modified};
    }
};

